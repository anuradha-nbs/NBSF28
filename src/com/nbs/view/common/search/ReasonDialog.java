    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        List<Object> inputs = new ArrayList<>();
        try {

            inputs.add(reasonID);
            inputs.add(txtReasonAmali.getText());
            inputs.add(txtReasonUnicode.getText());
            inputs.add(txtReasonGLCode.getId());
            inputs.add(txtReasonWargaya.getText());

            ServerConnection.getServerConnector().insertData(inputs, "ssp_Act_Insert_Act_Trn_Reason", 0);
        } catch (RemoteException ex) {
            Logger.getLogger(FindSectionOrOutsider.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FindSectionOrOutsider.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FindSectionOrOutsider.class.getName()).log(Level.SEVERE, null, ex);
        }
        FindSectionOrOutsider.refreshItem.doClick();
        dispose();
        reasonID = 0;
    }//GEN-LAST:event_jButton3ActionPerformed

