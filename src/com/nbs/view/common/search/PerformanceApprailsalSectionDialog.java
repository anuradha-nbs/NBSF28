    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            List inputs = new ArrayList();
            inputs.add(sectionID);
            inputs.add(txtSectionAmali.getText());
            inputs.add(txtSectionUnicode.getText());
            inputs.add(txtSuperGroup.getText());
            inputs.add("@recID");

            ServerConnection.getServerConnector().insertData(inputs, "ssp_Act_Insert_Act_Sections_Main", 1);
        } catch (RemoteException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
        FindSectionOrOutsider.refreshItem.doClick();
        dispose();
        sectionID = 0;
    }//GEN-LAST:event_jButton4ActionPerformed

