    private void txtSectionUnicodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSectionUnicodeKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_F2) {
            new UnicodeKeyBoard(this, true, txtSectionAmali,txtSectionUnicode,false).setVisible(true);
        }else if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F3) {
            new KeyBoard(true, txtSectionUnicode).setVisible(true);
        }
    }//GEN-LAST:event_txtSectionUnicodeKeyReleased

