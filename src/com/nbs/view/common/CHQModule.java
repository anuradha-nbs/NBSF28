private void txtAmountFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtAmountFocusLost
    try {
        if (txtAmount.getText().trim().isEmpty()) {
            txtAmount.setText("0.00");
        }
        txtAmount.setText(FormatConstants.decimalFormat.parse(txtAmount.getText().trim()).doubleValue() + "");//(formatter.format(Double.parseDouble(txtAmount.getText().trim())));
        if (FormatConstants.decimalFormat.parse(txtAmount.getText()).doubleValue() > 0) {
            btnChequeMax.setEnabled(false);
            btnCheque.setEnabled(true);
            txtMaxAmount.setText("0.00");
        } else {
            btnChequeMax.setEnabled(true);
            btnCheque.setEnabled(false);
        }
    } catch (ParseException ex) {
        Logger.getLogger(CHQModule.class.getName()).log(Level.SEVERE, null, ex);
    }
}//GEN-LAST:event_txtAmountFocusLost

