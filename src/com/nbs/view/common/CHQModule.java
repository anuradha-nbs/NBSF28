    private void txtMaxAmountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtMaxAmountActionPerformed
        try {
            if (txtMaxAmount.getText().trim().isEmpty()) {
                txtMaxAmount.setText("0.00");
            }
            txtMaxAmount.setText(FormatConstants.decimalFormat.parse((txtMaxAmount.getText().trim())).doubleValue() + "");
            if (FormatConstants.decimalFormat.parse(txtMaxAmount.getText()).doubleValue() > 0) {
                btnChequeMax.setEnabled(true);
                btnCheque.setEnabled(false);
                txtAmount.setText("0.00");
            } else {
                btnChequeMax.setEnabled(false);
                btnCheque.setEnabled(true);
            }
        } catch (ParseException ex) {
            Logger.getLogger(CHQModule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtMaxAmountActionPerformed

