    private void textFieldFocusLost(JTextField txtField) {
        if (txtField.getText().trim().substring(txtField.getText().trim().length() - 3).equals(".00")) {
            txtField.setText(txtField.getText());
        } else if (txtField.getText().trim().substring(txtField.getText().trim().length() - 2).equals(".0")) {
            txtField.setText(txtField.getText() + "0");
        } else if (txtField.getText().trim().substring(txtField.getText().trim().length() - 1).equals(".")) {
            txtField.setText(txtField.getText() + "00");
        } else if (!txtField.getText().trim().substring(txtField.getText().trim().length() - 3).equals(".00")) {
            txtField.setText(txtField.getText() + ".00");
        }
    }
