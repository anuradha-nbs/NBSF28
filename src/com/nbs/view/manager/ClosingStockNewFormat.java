private void dataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTableMouseClicked
    if (dataTable.getSelectedRow() > -1) {
        int selectedRow = dataTable.getSelectedRow();
        JTextField fields[] = {txtSubSection, txtBuying, txtSelling};
        for (int i = 0; i < dataTable.getColumnCount(); i++) {
            switch (i) {
                case 0:
                    txtSubSection.setText(String.valueOf(dataTable.getValueAt(selectedRow, 0)));
                case 1:
                    txtBuying.setText(String.valueOf(dataTable.getValueAt(selectedRow, 1)));
                case 2:
                    txtSelling.setText(String.valueOf(dataTable.getValueAt(selectedRow, 2)));
                case 3:
                    txtSubSection.setId(Integer.parseInt(String.valueOf(dataTable.getValueAt(selectedRow, 3))));
                case 4:
                    dtid = Integer.parseInt(String.valueOf(dataTable.getValueAt(selectedRow, 4)));
                    break;
                default:
                    break;
            }
        }

    }
}//GEN-LAST:event_dataTableMouseClicked

