    private void txtSellingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSellingActionPerformed
        JTextField fields[] = {txtBuying, txtSelling};
        for (JTextField field : fields) {
            if (field.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "යම් මාසයක දත්ත ඇතුලත් වී නොමැත. කරුණාකර නැවත පිරික්සන්න.", "කරුණාකර නැවත පිරික්සන්න.", JOptionPane.ERROR_MESSAGE);
                return;

            }
        }

        saveClosingStock();
        month = jMonthChooser1.getMonth()+1;
        viewDT(cmbFinancialYear.getSelectedItem().getId(), month);
        initTxtFields();
        txtSubSection.grabFocus();
        dtid = 0;
    }//GEN-LAST:event_txtSellingActionPerformed

